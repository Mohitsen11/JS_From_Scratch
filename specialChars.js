// we have \n \t \\ etc special chars in js

// \n is used for breaking a sentence with a newline

let text = "Hello, Mr. Mohit, \nWhat are you doing today?";

console.log(text);

// \t is tab space 

let text1 = "Beautiful life of my\tchildren";

console.log(text1);

// \\ use for printing backslash in a string

let example = "We can sing\\dance.";

console.log(example);